// scripts/write-assetsignore.mjs — REVIEW FIX M2 (Codex review) + Plan 04 Rule 2
//
// Two postbuild jobs:
//
// 1. Write dist/.assetsignore (REVIEW FIX M2).
//    Cloudflare Static Assets uploads everything in `assets.directory` (set
//    to ./dist in wrangler.jsonc) UNLESS .assetsignore tells it to skip a
//    path. Older @astrojs/cloudflare versions emitted dist/_worker.js into
//    the SAME dist/ tree; without this file, that Worker source could be
//    served as a downloadable public asset. Astro 6.3 + adapter 13.5 emits
//    the Worker at dist/server/entry.mjs and writes its own dist/client/.assetsignore,
//    so the dist/.assetsignore is defensive: if a future adapter version moves
//    things back, the file is already in place. Entries:
//      - _worker.js, _worker.js/**, _routes.json
//
// 2. Delete unreferenced dist/client/_astro/*.js bundles (Plan 04 Rule 2 auto-fix).
//    @astrojs/react preemptively emits a React-runtime client.<hash>.js bundle
//    even when no page uses a `client:` directive. The bundle is NEVER
//    referenced by any HTML file (verified by scanning dist/client/**/*.html
//    below) and exists only in the SSR asset manifest as a fallback. The
//    Phase 1 contract is "no `client:` directive → zero React in the
//    browser"; this cleanup honors that contract literally and removes ~190KB
//    of dead weight from the deploy bundle. Pages have export const prerender
//    = true; no SSR route falls back to the React bundle.

import { promises as fs } from 'node:fs';
import path from 'node:path';

const DIST = 'dist';
const TARGET = path.join(DIST, '.assetsignore');

const CONTENTS = `# Generated by scripts/write-assetsignore.mjs — DO NOT EDIT BY HAND.
# Phase 1 of Studio Bluemli site. REVIEW FIX M2 (Codex review).
# Cloudflare Static Assets uses this file to skip uploading server-side Worker
# output as public static assets. See:
# https://developers.cloudflare.com/workers/static-assets/binding/

_worker.js
_worker.js/**
_routes.json
`;

// Confirm dist/ exists — if not, astro build hasn't run yet.
try {
  const stat = await fs.stat(DIST);
  if (!stat.isDirectory()) throw new Error(`${DIST} exists but is not a directory`);
} catch (err) {
  console.error(`FAIL: ${DIST}/ not found. Run 'astro build' first.`);
  process.exit(1);
}

await fs.writeFile(TARGET, CONTENTS);
console.log(`Wrote ${TARGET}`);

// ----- Job 2: unreferenced _astro/*.js cleanup -----

const CLIENT_DIR = path.join(DIST, 'client');
const ASTRO_DIR = path.join(CLIENT_DIR, '_astro');

async function exists(p) {
  try { await fs.stat(p); return true; } catch { return false; }
}

if (await exists(CLIENT_DIR) && await exists(ASTRO_DIR)) {
  // Walk dist/client/**/*.html and collect every JS reference.
  /** @returns {Promise<string[]>} flat list of all .html files under root */
  async function walkHtml(root) {
    const results = [];
    const entries = await fs.readdir(root, { withFileTypes: true });
    for (const entry of entries) {
      const p = path.join(root, entry.name);
      if (entry.isDirectory()) {
        results.push(...(await walkHtml(p)));
      } else if (entry.isFile() && entry.name.endsWith('.html')) {
        results.push(p);
      }
    }
    return results;
  }

  const htmlFiles = await walkHtml(CLIENT_DIR);
  const referencedHashes = new Set();
  for (const html of htmlFiles) {
    const text = await fs.readFile(html, 'utf8');
    // Match any reference to /_astro/<filename>.js — capture the filename.
    const re = /_astro\/([A-Za-z0-9._-]+\.js)/g;
    let m;
    while ((m = re.exec(text)) !== null) {
      referencedHashes.add(m[1]);
    }
  }

  const astroEntries = await fs.readdir(ASTRO_DIR, { withFileTypes: true });
  let deleted = 0;
  for (const entry of astroEntries) {
    if (!entry.isFile()) continue;
    if (!entry.name.endsWith('.js')) continue;
    if (referencedHashes.has(entry.name)) continue;
    // Unreferenced JS bundle — delete it.
    const filePath = path.join(ASTRO_DIR, entry.name);
    await fs.unlink(filePath);
    console.log(`Removed unreferenced bundle: ${filePath}`);
    deleted++;
  }
  console.log(`Cleanup: ${deleted} unreferenced _astro/*.js file(s) removed; ${referencedHashes.size} retained.`);
} else {
  console.log('No dist/client/_astro directory — skipping unreferenced-bundle cleanup.');
}
